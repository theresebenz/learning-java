public class SwitchStatement {
    public static void main(String[] args) {
        int num = 1;

        switch(num) {
            case 1:
                System.out.println("case1");
                break;
            case 3:
                System.out.println("case3");
                break;
            default:
                System.out.println("default");
                System.out.println("default");
                System.out.println("default");
                break;
        }
    }
}
